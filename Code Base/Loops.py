#For loops

#Basic loop, Goes through all the names
names = ["Alice", "Bob", "Charlie"]

for name in names:
    print("Hello, " + name + "!")

#Counting loop
#Goes through all the numbers 0-9
for i in range(10):
    print(i)




#######################################################################################
#While Loops
#Stays within the loop as long as stays true
a = 1

while a < 2000:
    print(a)
    a = a * 2